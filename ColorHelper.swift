import UIKit

class ColorHelper{

    func Black() -> UIColor {
        return UIColor(red: 0 / 255, green: 0 / 255, blue: 0 / 255, alpha: 1.0)
    }

    func White() -> UIColor {
        return UIColor(red: 255 / 255, green: 255 / 255, blue: 255 / 255, alpha: 1.0)
    }
    
    func ButterflyBush() -> UIColor {
        return UIColor(red: 103 / 255, green: 78 / 255, blue: 167 / 255, alpha: 1.0)
    }

}
